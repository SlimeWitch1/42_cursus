int	ft_strcmp(char *s1, char *s2)
{
	int	i;
	int	diff;

	i = 0;
	while (s1[i] != '\0' || s2[i] != '\0')
	{
		diff = 0;
		if (s1[i] != s2[i])
		{
			diff = 1;
			break ;
		}
		i++;
	}
	if (diff == 1)
		return (s1[i] - s2[i]);
	else
		return (0);
}
